package com.company;

public class Main {

    public static void main(String[] args) {
	// Program that performs all arithmetic operations and prints the result to the console
        int val1 = 12, val2 = 5;

        //Performing adding, adding val1 to val2
        System.out.println("val1 + val2 = " + (val1 + val2) );

        //Performing subtraction, subtract val1 from val2
        System.out.println("val1 - val2 = " + (val1 - val2) );

        //Performing multiplication, multiply val1 from val2
        System.out.println("val1 * val2 = " + (val1 * val2) );

        //Performing division, divide val2 from val1
        System.out.println("val1 / val2 = " + (val1 / val2) );

        //Performing modulo operation, reminder after division
        System.out.println("val1 % val2 = " + (val1 % val2) );




    }
}
